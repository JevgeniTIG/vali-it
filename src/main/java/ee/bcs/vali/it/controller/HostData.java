package ee.bcs.vali.it.controller;

import java.math.BigInteger;

public class HostData {
    private String hostName;
    private String hostLastName;
    private String hostEmail;
    private String hostPhone;
    private String hostLogin;
    private String hostPassword;
    private String hostService;
    private String hostAddress;
    private String hostAddressType;
    private BigInteger hostId;

    public String getHostName() {
        return hostName;
    }

    public void setHostName(String hostName) {
        this.hostName = hostName;
    }

    public String getHostLastName() {
        return hostLastName;
    }

    public void setHostLastName(String hostLastName) {
        this.hostLastName = hostLastName;
    }

    public String getHostEmail() {
        return hostEmail;
    }

    public void setHostEmail(String hostEmail) {
        this.hostEmail = hostEmail;
    }

    public String getHostPhone() {
        return hostPhone;
    }

    public void setHostPhone(String hostPhone) {
        this.hostPhone = hostPhone;
    }

    public String getHostLogin() {
        return hostLogin;
    }

    public void setHostLogin(String hostLogin) {
        this.hostLogin = hostLogin;
    }

    public String getHostPassword() {
        return hostPassword;
    }

    public void setHostPassword(String hostPassword) {
        this.hostPassword = hostPassword;
    }

    public String getHostService() {
        return hostService;
    }

    public void setHostService(String hostService) {
        this.hostService = hostService;
    }

    public String getHostAddress() {
        return hostAddress;
    }

    public void setHostAddress(String hostAddress) {
        this.hostAddress = hostAddress;
    }

    public String getHostAddressType() {
        return hostAddressType;
    }

    public void setHostAddressType(String hostAddressType) {
        this.hostAddressType = hostAddressType;
    }

    public BigInteger getHostId() {
        return hostId;
    }

    public void setHostId(BigInteger hostId) {
        this.hostId = hostId;
    }
}
